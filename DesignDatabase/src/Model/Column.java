package Model;

import java.util.Objects;

public class Column {
    private String name;
    private Object type;

    public Column(String name, Object type) {
        this.name = name;
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Object getType() {
        return type;
    }

    public void setType(Object type) {
        this.type = type;
    }
}
